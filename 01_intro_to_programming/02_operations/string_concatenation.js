console.log("Hello, " + "World!"); // Hello, World!
console.log("1" + "2"); // "12"
console.log("1" + 2); // "12", number coerced to string and concatenated
console.log(1 + "2"); // "12", number coerced to string and concatenated

// Other arithmetic operators
console.log("5" - 3); // 2, string coerced to number and operated on as number
console.log("5" / "2"); // 2.5 strings coerced to number and operated on as number
console.log("2" * "5"); // 10 string coerced to number and operated on as number
