// Run in node:

// > let a = 4;
// undefined

// > let b = a;
// undefined

// > a = 7;
// 7

// > b
// 4

/*

b retains the value of 4 even though a is reassigned to value of 7. This shows that variables have values that are not deeply linked to each other. If you change one variable it does not change other variables with the same value.



*/