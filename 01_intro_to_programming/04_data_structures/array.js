// array literal: contains integers between 1 and 4
let array = [1, 2, 3, 4];
console.log(array);

// access array element value by non negative index starting at 0
console.log(array[0]);
console.log(array[1]);
console.log(array[2]);
console.log(array[3]);


// attempt to access a value that has not been defined
console.log(array['5']); // undefined
console.log(array[5]); // undefined
