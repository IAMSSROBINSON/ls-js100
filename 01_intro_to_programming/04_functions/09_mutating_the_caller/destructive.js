let oddNumbers = [3, 5, 7, 9]; // [ 3, 5, 7, 9]

console.log(oddNumbers.pop()); // 9
console.log(oddNumbers); // [ 3, 5, 7 ]

/*

.pop() methods removes the last element in an array. 
.pop() is destructive in that the array returned by the method has permanently removed the last element. 
.pop() mutates the caller, the object upon which it was called (the array).

*/