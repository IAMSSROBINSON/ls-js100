// store any datatype in an array
let heterogeneousArray = ["string", 100, true, {color: 'yellow'}, [10, 20]];
console.log(heterogeneousArray);

// indexed, ordered lists
console.log(heterogeneousArray[2]);

// the first element is at index 0
console.log(heterogeneousArray[0]);

// access the last element of the array no matter how many elements
console.log(heterogeneousArray[heterogeneousArray.length - 1]);



