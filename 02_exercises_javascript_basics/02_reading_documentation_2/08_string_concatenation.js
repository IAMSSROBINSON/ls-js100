// Find out how we can join two or more strings together.

let firstWord= "The";
let secondWord = "Republic"
let fullWord = firstWord.concat(" ", secondWord); // .concat() return a new string

console.log(firstWord + " " + secondWord); // prints strings as they are
console.log(fullWord);

// you can concatenate strings using the .concat() method
// arguments accepted are separated by a comma
// the + symbol will also concatenate string dataType elements